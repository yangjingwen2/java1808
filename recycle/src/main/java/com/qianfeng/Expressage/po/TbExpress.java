package com.qianfeng.Expressage.po;


public class TbExpress {

  private long expId;
  private String expName;
  private String expNum;
  private String expOrderNum;


  public long getExpId() {
    return expId;
  }

  public void setExpId(long expId) {
    this.expId = expId;
  }


  public String getExpName() {
    return expName;
  }

  public void setExpName(String expName) {
    this.expName = expName;
  }


  public String getExpNum() {
    return expNum;
  }

  public void setExpNum(String expNum) {
    this.expNum = expNum;
  }


  public String getExpOrderNum() {
    return expOrderNum;
  }

  public void setExpOrderNum(String expOrderNum) {
    this.expOrderNum = expOrderNum;
  }
}
